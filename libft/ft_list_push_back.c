#include "libft.h"

void	ft_list_push_back(t_list **begin_list, void *content)
{
	t_list	*list;

	list = *begin_list;
	if (list)
	{
		while (list->next)
			list = list->next;
		list->next = ft_create_elem(content);
	}
	else
		*begin_list = ft_create_elem(content);
}
